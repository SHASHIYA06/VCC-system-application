-- Create Drawing Verification Status for all drawings
INSERT INTO "DrawingVerificationStatus" (
  id, "drawingId", "drawingNumber", status, "verificationDate", "verifiedBy", 
  notes, "pagesVerified", "totalPages", confidence, extra, "createdAt", "updatedAt"
)
SELECT 
  gen_random_uuid() as id,
  d.id as "drawingId",
  d."drawingNo" as "drawingNumber",
  CASE 
    WHEN d."isSynced" THEN 'VERIFIED' 
    ELSE 'UNVERIFIED' 
  END as status,
  CASE 
    WHEN d."syncedAt" IS NOT NULL THEN d."syncedAt"
    WHEN d."isSynced" THEN NOW()
    ELSE NULL
  END as "verificationDate",
  'System' as "verifiedBy",
  CASE 
    WHEN d."isSynced" THEN 'Auto-verified during schema upgrade'
    ELSE 'Pending verification'
  END as notes,
  COALESCE(d."totalSheets", 1) as "pagesVerified",
  COALESCE(d."totalSheets", 1) as "totalPages",
  CASE 
    WHEN d."isSynced" THEN 0.95 
    WHEN d."totalSheets" > 0 THEN 0.85
    ELSE 0.75 
  END as confidence,
  jsonb_build_object(
    'autoGenerated', true, 
    'source', 'schema_migration_20260717',
    'connectorCount', (SELECT COUNT(*) FROM "Connector" c WHERE c."drawingId" = d.id),
    'wireCount', (SELECT COUNT(*) FROM "Wire" w WHERE EXISTS (
      SELECT 1 FROM "DrawingWire" dw WHERE dw."drawingId" = d.id AND dw."wireId" = w.id
    ))
  ) as extra,
  NOW() as "createdAt",
  NOW() as "updatedAt"
FROM "Drawing" d
WHERE NOT EXISTS (
  SELECT 1 FROM "DrawingVerificationStatus" dvs 
  WHERE dvs."drawingId" = d.id
);

-- Create basic Equipment records from Device data
-- This creates a simplified equipment catalog
INSERT INTO "Equipment" (
  id, name, code, "subsystemId", manufacturer, model, "partNumber", 
  description, metadata, "createdAt", "updatedAt"
)
SELECT 
  gen_random_uuid() as id,
  DISTINCT 
  d."deviceName" as name,
  UPPER(REPLACE(REPLACE(d."deviceName", ' ', '_'), '/', '_')) as code,
  d."subsystemId" as "subsystemId",
  COALESCE(d."manufacturerRef", 'Unknown') as manufacturer,
  NULL as model,
  d."tagNo" as "partNumber",
  CONCAT('Auto-generated from device: ', d."deviceName") as description,
  jsonb_build_object(
    'autoGenerated', true,
    'source', 'device_data_migration',
    'originalTagNo', d."tagNo",
    'deviceCount', COUNT(*) OVER (PARTITION BY d."deviceName")
  ) as metadata,
  NOW() as "createdAt",
  NOW() as "updatedAt"
FROM "Device" d
WHERE d."deviceName" IS NOT NULL
  AND LENGTH(d."deviceName") > 0
  AND NOT EXISTS (
    SELECT 1 FROM "Equipment" e 
    WHERE e.code = UPPER(REPLACE(REPLACE(d."deviceName", ' ', '_'), '/', '_'))
  )
ORDER BY d."deviceName";

