-- Create Cable records from wire data
INSERT INTO "Cable" (
  id, "cableNumber", "cableType", "coreCount", manufacturer, "partNumber",
  routing, length, description, metadata, status, "createdAt", "updatedAt"
)
SELECT 
  gen_random_uuid() as id,
  DISTINCT w."cableSpec" as "cableNumber",
  CASE 
    WHEN w."wireSize" LIKE '%x%' THEN 'MULTI_CORE'
    ELSE 'SINGLE_CORE'
  END as "cableType",
  NULL as "coreCount",
  'Unknown' as manufacturer,
  NULL as "partNumber",
  NULL as routing,
  NULL as length,
  CONCAT('Auto-generated from wire specification: ', w."cableSpec") as description,
  jsonb_build_object(
    'autoGenerated', true,
    'source', 'wire_data_migration',
    'wireCount', COUNT(*) OVER (PARTITION BY w."cableSpec"),
    'voltageClasses', ARRAY_AGG(DISTINCT w."voltageClass") FILTER (WHERE w."voltageClass" IS NOT NULL)
  ) as metadata,
  'active' as status,
  NOW() as "createdAt",
  NOW() as "updatedAt"
FROM "Wire" w
WHERE w."cableSpec" IS NOT NULL
  AND LENGTH(w."cableSpec") > 0
  AND NOT EXISTS (
    SELECT 1 FROM "Cable" c 
    WHERE c."cableNumber" = w."cableSpec"
  )
ORDER BY w."cableSpec";

